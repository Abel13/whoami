export function useCategoryOptions(category: string) {
  const [options, setOptions] = useState(
    categories.find((c) => c.key === category).items
  );
  const [usedOptions, setUsedOptions] = useState<string[]>([]);

  const getNextOption = (): string | null => {
    if (options.length === 0) {
      return null;
    }

    const randomIndex = Math.floor(Math.random() * options.length);
    const nextOption = options[randomIndex];

    setOptions((prev) => prev.filter((_, index) => index !== randomIndex));
    setUsedOptions((prev) => [...prev, nextOption]);

    return nextOption;
  };

  const resetRound = () => {
    setOptions([...categories.find((c) => c.key === category).items]);
    setUsedOptions([]);
  };

  return {
    options,
    usedOptions,
    getNextOption,
    resetRound,
  };
}
